@main.route('/user/<username>', methods=['GET', 'POST'])
def user(username):
    """ 查看指定用户信息
    :param username: 指定用户
    """
    _user = User.query.filter_by(username=username).first()
    if _user is None:
        abort(404)
    page = request.args.get('page', 1, type=int)
    pagination = _user.posts.order_by(Post.timestamp.desc()).paginate(page, per_page=current_app.config[
        'FLASK_POSTS_PER_PAGE'], error_out=False)
    posts = pagination.items
    if current_user.avatar_path != 'upload/default_man_head.png':
        img_url = _user.avatar_path
    else:
        img_url = '/uploaded/default_man_head.png/'  # http://127.0.0.1:5050/images/lqx.jpg
    if request.method == 'POST':
        print("进行")
        file = request.files.get('photo')
        if file and allowed_file(file.filename):
            # 获取文件的后缀名
            suffix = os.path.splitext(file.filename)[1]
            # 生成随机文件名
            filename = random_string() + suffix
            pathname = os.path.join(current_app.config['UPLOADED_FOLDER'], filename)
            file.save(pathname)
            # 生成缩略图分为三步
            # 1.打开图片
            img = Image.open(pathname)
            # 2.重新设置尺寸
            img.thumbnail((128, 128))
            # 3.保存s
            img.save(pathname)
            img_url = url_for('main.uploaded', filename=filename)
            current_user.avatar_path = img_url
            db.session.add(current_user)
            db.session.commit()
            flash('更新头像成功！')
    return render_template('user.html', posts=posts, user=_user, pagination=pagination, img_url=img_url)


@main.route('/post/<int:id>', methods=['GET', 'POST'])
def post(id):
    """ 发表评论

    :param id: 微博 id
    """
    _post = Post.query.get_or_404(id)
    form = CommentForm()
    if form.validate_on_submit():
        comment = Comment(body=form.body.data, post=_post, author=current_user._get_current_object())
        # comment 的 author 字段不能直接设置为 current_user，因为这个变量是上下文代理
        # 对象。真正的 User 对象要使用 current_user._get_current_object() 获取
        db.session.add(comment)
        db.session.commit()
        flash('您已经成功提交评论')
        return redirect(url_for('.post', id=_post.id, page=-1))
    page = request.args.get('page', 1, type=int)
    if page == -1:
        print("-1")
        page = (_post.comments.count() - 1) / current_app.config['FLASK_POSTS_PER_PAGE'] + 1  # 20 为每页评论条数
        page = 1
    pagination = _post.comments.order_by(Comment.timestamp.asc()).paginate(page, per_page=current_app.config[
        'FLASK_POSTS_PER_PAGE'], error_out=False)
    comments = pagination.items
    return render_template("post.html", posts=[_post], form=form, pagination=pagination, comments=comments)


@main.route('/edit/<int:id>', methods=['GET', 'POST'])
@login_required
def edit(id):
    """ 更新微博

    :param id: 微博 id
    """
    _post = Post.query.get_or_404(id)
    if current_user != _post.author and not current_user.can(Permission.ADMINISTER):
        abort(403)
    form = PostForm()
    if form.validate_on_submit():
        _post.body = form.body.data
        db.session.add(_post)
        db.session.commit()
        flash("微博已经更新")
        return redirect(url_for('.post', id=_post.id))
    form.body.data = _post.body
    return render_template('edit_post.html', form=form)


