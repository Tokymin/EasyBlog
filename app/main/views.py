@main.route('/', methods=['GET', 'POST'])
def index():
    """ 视图首页，发表微博
    """
    form = PostForm()
    if form.validate_on_submit() and current_user.can(Permission.WRITE_ARTICLES):
        _post = Post(body=form.body.data, author=current_user._get_current_object())
        db.session.add(_post)
        db.session.commit()
        return redirect(url_for('main.index'))
    _show_followed = False
    if current_user.is_authenticated:
        _show_followed = bool(request.cookies.get('show_followed', ''))
    if _show_followed:
        query = current_user.followed_posts
    else:
        query = Post.query

    page = request.args.get('page', 1, type=int)  # 默认为第 1 页
    pagination = query.order_by(Post.timestamp.desc()).paginate(page,
                                                                per_page=current_app.config['FLASK_POSTS_PER_PAGE'],
                                                                error_out=False)
    posts = pagination.items
    return render_template('index.html', form=form, posts=posts, pagination=pagination)


